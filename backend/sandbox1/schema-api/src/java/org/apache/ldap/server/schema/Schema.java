/**
 * Comments are quoted, paraphrased, and otherwise just pulled from 
 * RFC 2251, December 1997 (Wahl, Howes and Kille) Additional clarifications 
 * are added when necessary, but not often. 
 *
 * Schema is the collection of attribute type definitions, object class 
 * definitions and other information which a server uses to determine how 
 * to match a filter or attribute value assertion (in a compare operation) 
 * against the attributes of an entry, and whether to permit add and modify 
 * operations. The definition of schema for use with LDAP is given in 
 * [1] and [2]. Additional schema elements may be defined in other documents. 
 * 
 * Entries consist of a set of attributes. An attribute is a type with one or 
 * more associated values. The attribute type is identified by a short 
 * descriptive name and an OID (object identifier). The attribute type governs 
 * whether there can be more than one value of an attribute of that type in an 
 * entry, the syntax to which the values must conform, the kinds of matching 
 * which can be performed on values of that attribute, and other functions. 
 *
 * An example of an attribute is "mail". There may be one or more values of 
 * this attribute, they must be IA5 (ASCII) strings, and they are case 
 * insensitive (e.g. "foo@bar.com" will match "FOO@BAR.COM").
 * 
 * Each entry MUST have an objectClass attribute. The objectClass attribute 
 * specifies the object classes of an entry, which along with the system and 
 * user schema determine the permitted attributes of an entry. Values of this 
 * attribute may be modified by clients, but the objectClass attribute cannot 
 * be removed. Servers may restrict the modifications of this attribute to 
 * prevent the basic structural class of the entry from being changed (e.g. 
 * one cannot change a person into a country). When creating an entry or adding 
 * an objectClass value to an entry, all superclasses of the named classes are 
 * implicitly added as well if not already present, and the client must supply 
 * values for any mandatory attributes of new superclasses. 
 *
 * Some attributes, termed operational attributes, are used by servers for 
 * administering the directory system itself. They are not returned in search 
 * results unless explicitly requested by name. Attributes which are not 
 * operational, such as "mail", will have their schema and syntax constraints 
 * enforced by servers, but servers will generally not make use of their values.
 * 
 * [1] Wahl, M., Coulbeck, A., Howes, T., and S. Kille, "Lightweight 
 *      Directory Access Protocol (v3): Attribute Syntax Definitions", 
 *      RFC 2252, December 1997. 
 * [2] ITU-T Rec. X.501, "The Directory: Models", 1993. 
 * 
 * @author <a href="mailto:aok123@bellsouth.net">Alex Karasulu</a>
 * @author $Author: akarasulu $
 * @version $Revision: 1.5 $
 */
public interface Schema
{

    /**
     * Apply the appropriate normalizer to the given attribute value.
     * The appropriate normalizer is identified by the EQUALITY 
     * setting of the named attribute type.
     *
     * @param an_attributeName  The attribute type name
     * @param an_attributeValue The attribute value to normalize
     * @return String representing the normalized value of the given value
     * @throws NamingException When the attribute type specified is not
     *                         available from this schema.
     */
    String normalize( String an_attributeName, String an_attributeValue )
        throws NamingException ;
    
    /**
     * return the appropriate normalizer as identified by the EQUALITY 
     * setting of the given OID or named attribute type. Setting the 
     * byName argument to false will retieve the Normalizer based 
     * on the IANA OID represented in key. Setting the byName argument
     * to true will return the Noramlizer based on the attribute name
     * given in the key.
     *
     * @param a_key    Either the OID or the attribute name
     * @param a_byName false if key is an OID, true if attribute name
     * @return Normalizer The appropriate normalizer for the itentified
     *                    attribute type
     * @throws NamingException if the attribute type is not available
     *                         in this schema.
     */
    Normalizer getNormalizer( String a_key, boolean a_byName )
        throws NamingException ; 
        
    /**
     * 
     * TODO Document me!
     *
     * @param an_entry TODO
     * @throws SchemaViolationException TODO
     * @throws InvalidAttributesException TODO
     */
    void check( Attributes an_entry )
        throws SchemaViolationException, InvalidAttributesException ;

    /**
     * Entries MAY contain, among others, the following operational attributes, 
     * defined in [1]. These attributes are maintained 
     * automatically by the server and are not modifiable by clients: 
     *
     * <pre>
     * <li>creatorsName: the Distinguished Name of the user who added this
     *                   entry to the directory.</li>
     * <li>createTimestamp: the time this entry was added to the directory.</li>
     * <li>modifiersName: the Distinguished Name of the user who last modified
     *                    this entry.</li>
     * <li>modifyTimestamp: the time this entry was last modified.</li>
     * <li>subschemaSubentry:  the Distinguished Name of the subschema entry
     *                         (or subentry) which controls the schema for 
     *                         this entry.</li>
     *
     * @param an_attributeName String representation of the entry name
     * @return true if the named attribute is operational, false otherwise
     */
    boolean isOperational( String an_attributeName ) ;
    
    /**
     * Does the named attribute type support multiple values.
     *
     * @param an_attributeName Named attribute type
     * @return boolean
     */
    boolean isMultiValue( String an_attributeName ) ;
    
    /**
     * Is the named attribute type single value only
     *
     * @param an_attributeName Named attribute type
     * @return boolean
     */
    boolean isSingleValue( String an_attributeName ) ;
    
    /**
     * Servers MUST NOT permit clients to add attributes to an entry unless 
     * those attributes are permitted by the object class definitions, the 
     * schema controlling that entry (specified in the subschema), or are 
     * operational attributes known to that server and used for administrative 
     * purposes. Note that there is a particular objectClass 'extensibleObject' 
     * defined in [1] which permits all user attributes to be present in an 
     * entry. 
     *
     * Validate the named attribute is contained within the schema and return
     * true. If the named attribute us not contained within the schema, return
     * false.
     * 
     * @param an_attributeName The attribute name to check
     * @return boolean true if the named attribute is in the schema,
     *                 false otherwise.
     */
    boolean hasAttribute( String an_attributeName ) ;
    
    /**
     * We need to create regular expressions for the various
     * syntax types and use them to see if we match.
     *
     * Is the given attribute value syntactically/content appropriate with
     * the SYNTAX specified for the named attribute type.
     * 
     * @param an_attributeName  The named attribute type.
     * @param an_attributeValue The value to evaluate
     * @return boolean true is syntactically correct, false otherwise
     */
    boolean isValidSyntax( String an_attributeName, Object an_attributeValue ) ;
    
    /**
     * We need to create regular expressions for the various
     * syntax types and use them to see if we match.
     *
     * Are the given attribute values syntactically/content appropriate with
     * the SYNTAX specified for the named attribute type.
     * 
     * @param an_attributeName  The named attribute type.
     * @param an_attributeValue The array of values to evaluate
     * @return boolean true is syntactically correct, false otherwise
     */
    boolean isValidSyntax( String an_attributeName, 
        Object [] an_attributeValue ) ;
        
    /**
     * Is the named attribute type a binary type.
     *
     * @param an_attributeName Named attribute type
     * @return boolean true if the attribute type is boolean, false otherwise
     */
    boolean isBinary( String an_attributeName ) ;
    
    /**
     * Is the named attribute type a numeric type.
     *
     * @param an_attributeName Named attribute type
     * @return boolean true if the attribute type is numeric, false otherwise
     */
    boolean isNumeric( String an_attributeName ) ;
    
    /**
     * Is the named attribute type a decimal type.
     *
     * @param an_attributeName Named attribute type
     * @return boolean true if the attribute type is decimal, false otherwise
     */
    boolean isDecimal( String an_attributeName ) ;
    
    /**
     * 
     * TODO Document me!
     *
     * @return TODO
     */
    NameParser getNameParser() ;
    
    /**
     * 
     * TODO Document me!
     *
     * @return TODO
     */
    NameParser getNormalizingParser() ;
    
    /**
     * TODO Document me!
     *
     * @return LdifParser
     */
    LdifParser getLdifParser() ;
    
    /**
     * 
     * TODO Document me!
     *
     * @return LdifComposer
     */
    LdifComposer getLdifComposer() ;
    
    /**
     * Retrieve the AttributeType object associated with the given
     * OID if the byName boolean is false. Otherwise retrieve the
     * AttributeType based upon the given name.
     *
     * @param a_key     Attribute OID if byName = false, name id byName=true
     * @param a_byName  False if a_key is an OID, true id a_key is a name
     * @return AttributeType object populated based upon the given key
     */
    AttributeType getAttributeSpec( String a_key, boolean a_byName ) ;
    
    /**
     * Retrieve the ObjectClass object associated with the given
     * OID if the byName boolean is false. Otherwise retrieve the
     * ObjectClass based upon the given name.
     *
     * @param a_key     ObjectClass OID if byName = false, name id byName=true
     * @param a_byName  False if a_key is an OID, true id a_key is a name
     * @return ObjectClass object populated based upon the given key
     */
    ObjectClass getObjectClassSpec( String a_key, boolean a_byName ) ;
}
