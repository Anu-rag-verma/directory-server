package org.apache.ldap.server.schema.syntax ;


import javax.naming.NamingException ;
import javax.naming.directory.Attribute ;
import javax.naming.directory.Attributes ;


import org.apache.avalon.framework.configuration.Configuration ;
import org.apache.avalon.framework.configuration.ConfigurationException ;


/**
 * Each attribute stored in a LDAP directory has a defined syntax (i.e. data 
 * type) which constrains the structure and format of its values.  The 
 * description of each syntax specifies how attribute or assertion values 
 * conforming to the syntax are normally represented when transferred in LDAP 
 * operations.  This representation is referred to as the LDAP-specific 
 * encoding to distinguish it from other methods of encoding attribute values.
 * 
 * @see <a href="http://www.faqs.org/rfcs/rfc2252.html">RFC 2252</a>
 * 
 * @author <a href="mailto:aok123@bellsouth.net">Alex Karasulu</a>
 * @author $Author: akarasulu $
 * @version $Revision: 1.4 $
 */
public class SyntaxImpl implements Syntax
{
    /** 
     * Default SyntaxChecker accepting the univeral set of values: its for the
     * binary syntax.  All information can be represented as binary and pass.
     */
    private static final SyntaxChecker ACCEPT_ALL = new SyntaxChecker()
    {
        public String getSyntaxOid() 
        { 
            return "1.3.6.1.4.1.1466.115.121.1.5" ;
        }

        public boolean isValidSyntax( Object a_value )
        {
            return true ;
        }
    
        public void assertSyntax( Object a_value ) throws NamingException 
        {
            return ;
        }
    } ;
    
    /** 
     * Transient definition for this syntax as specified in RFC 2252.
     * <code>
     * "(" whsp numericoid whsp [ "DESC" qdstring ] whsp ")"
     * </code>
     */
    private transient String m_definition ;
    /** Whether or not values of this syntax are human readable */
    private boolean m_isHumanReadable = true ;
    /** Object identifier of the syntax */
    private final String m_oid ;
    /** Descriptive name for the syntax */
    private String m_name ;
    /** Description for the syntax */
    private String m_desc ;
    /** SyntaxImpl Checker */
    private SyntaxChecker m_checker = ACCEPT_ALL ;

    
    
    // ------------------------------------------------------------------------
    // C O N S T R U C T O R S
    // ------------------------------------------------------------------------
    
    
    /**
     * Creates a SyntaxImpl instance with a unique object identifier.
     * 
     * @param a_oid the object identifier of the new SyntaxImpl.
     */
    SyntaxImpl( String a_oid )
    {
        m_oid = a_oid ;
    }
    
    
    // ------------------------------------------------------------------------
    // Member Accessor Mutator Methods
    // ------------------------------------------------------------------------
    
    
    /**
     * Gets whether this value SyntaxImpl is human readable.
     * 
     * @return whether or not this Syntax is for human readable data.
     */
    public boolean isHumanReadable( )
    {
        return m_isHumanReadable ;
    }


    /**
     * Sets whether this value SyntaxImpl is human readable.
     * 
     * @param a_isHumanReadable true if the values of this Syntax are human 
     *      readable, false otherwise.
     */
    void setHumanReadable( boolean a_isHumanReadable )
    {
        m_isHumanReadable = a_isHumanReadable ;
    }
    
    
    /**
     * Gets a description of this SyntaxImpl.
     * 
     * @return a description
     */
    public String getDesc()
    {
        return m_desc ;
    }


    /**
     * Sets the description for this SyntaxImpl
     * 
     * @param a_desc the description
     */
    void setDesc( String a_desc )
    {
        m_desc = a_desc ;
    }


    /**
     * Gets a short descriptive name for the SyntaxImpl. 
     * 
     * @return a short name.
     */
    public String getName()
    {
        return m_name ;
    }


    /**
     * Sets the name or label for this SyntaxImpl.
     * 
     * @param a_name a short name or label
     */
    void setName( String a_name )
    {
        m_name = a_name ;
    }


    /**
     * Gets the oid for this SyntaxImpl.
     * 
     * @return the object identifier
     */
    public String getOid()
    {
        return m_oid ;
    }
    

    /**
     * Gets the SyntaxChecker used by this SyntaxImpl.
     * 
     * @return the SyntaxChecker
     */
    public SyntaxChecker getSyntaxChecker()
    {
        return m_checker ;
    }


    /**
     * Sets the SyntaxChecker used by this SyntaxImpl.
     * 
     * @param a_checker the SyntaxChecker to use for this SyntaxImpl
     */
    void setChecker( SyntaxChecker a_checker )
    {
        m_checker = a_checker ;
    }


    /**
     * Gets or generates the SyntaxImpl Description for this SyntaxImpl as 
     * defined by the SyntaxImpl 1.3.6.1.4.1.1466.115.121.1.54.
     * 
     * <code>
     * "(" whsp numericoid whsp [ "DESC" qdstring ] whsp ")"
     * </code>
     * @return the SyntaxImpl Description
     */
    public String getSyntaxDescription()
    {
        if ( null == m_definition )
        {
            StringBuffer l_buf = new StringBuffer() ;
            l_buf.append( "( " ) ;
            l_buf.append( m_oid ) ;
            
            if ( null != m_desc )
            {
                l_buf.append( " DESC " ) ;
                l_buf.append( m_desc ) ;
            }
            
            l_buf.append( " )" ) ;
            
            m_definition = l_buf.toString() ;
        }
        
        return m_definition ;
    }

    
    /**
     * Builds SyntaxImpl instances from Configuration nodes and Attributes. 
     * 
     * @author <a href="mailto:aok123@bellsouth.net">Alex Karasulu</a>
     * @author $Author: akarasulu $
     */
    public static class Builder
    {
        /**
         * Creates a SyntaxImpl instance using a syntax Configuration node.   
         * Does not yet support the use of SyntaxCheckers.
         * 
         * @param a_config the syntax Configuration node
         * @return the SyntaxImpl represented by the Configuration node
         * @throws ConfigurationException if the Configuration is invalid
         */
        public static Syntax create( Configuration a_config ) 
            throws ConfigurationException
        {
            SyntaxImpl l_syntax = new SyntaxImpl( a_config.getChild( "oid" )
                .getValue() ) ;
            l_syntax.setDesc( a_config.getChild( "description" )
                .getValue( null ) ) ;
            l_syntax.setHumanReadable( a_config.getChild( "human-readable" )
                .getValueAsBoolean( true ) ) ;
            l_syntax.setName( a_config.getChild( "name" ).getValue( null ) ) ;
            return l_syntax ;
        }
        
        
        /**
         * Creates a SyntaxImpl instance using a syntax entry's Attributes.  
         * Does not yet support the use of SyntaxCheckers.
         * 
         * @param a_attributes the set of attributes of a syntax entry
         * @return the SyntaxImpl represented by the entry
         * @throws NamingException if the required attributes cannot be accessed
         * or are unavailable
         */
        public static Syntax create( Attributes a_attributes )
            throws NamingException
        {
            SyntaxImpl l_syntax = null ;
            
            Attribute l_attr = a_attributes.get( "oid" ) ;
            if ( null == l_attr || 0 == l_attr.size() )
            {
                throw new NamingException( "oid is undefined for " 
                    + a_attributes ) ;
            }
            
            l_syntax = new SyntaxImpl( ( String ) l_attr.get() ) ;

            l_attr = a_attributes.get( "description" ) ;
            if ( null != l_attr )
            {
                l_syntax.setDesc( ( String ) l_attr.get() ) ;
            }
            
            l_attr = a_attributes.get( "name" ) ;            
            if ( null != l_attr )
            {
                l_syntax.setName( ( String ) l_attr.get() ) ;
            }
            
            l_attr = a_attributes.get( "humanReadable" ) ;            
            if ( null != l_attr )
            {
                l_syntax.setHumanReadable( getBoolean( l_attr ) ) ;
            }
            
            return l_syntax ;
        }


        /**
         * Creates a SyntaxImpl instance using an oid.  
         * Primarily for debugging purposes.
         * 
         * @param a_oid The oid used to create the syntax checker
         * @return the SyntaxImpl represented by the entry
         * or are unavailable
         */
        public static Syntax create( String a_oid ) 
        {
            return new SyntaxImpl( a_oid );
        }


        /**
         * Gets a boolean from an Attribute.
         * 
         * @param a_attr the attribute to convert to a boolean
         * @return true if attribute has a '1', 'true' or 'yes' value false
         * otherwise
         * @throws NamingException on failure to access attribute data
         */
        private static boolean getBoolean( Attribute a_attr )
            throws NamingException
        {
            String l_value = ( String ) a_attr.get() ;
            
            return l_value.equals( "1" ) || 
                l_value.equalsIgnoreCase( "true" ) ||
                l_value.equalsIgnoreCase( "yes" ) ;
        }
    }
}
