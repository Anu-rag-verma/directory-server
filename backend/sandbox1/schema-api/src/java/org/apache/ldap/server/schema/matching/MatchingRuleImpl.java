package org.apache.ldap.server.schema.matching ;


import java.util.Comparator ;

import javax.naming.NamingException ;
import javax.naming.directory.Attribute ;
import javax.naming.directory.Attributes ;

import org.apache.ldap.common.NotImplementedException ;

import org.apache.ldap.server.schema.syntax.Syntax ;
import org.apache.ldap.server.schema.syntax.SyntaxManager ;

import org.apache.avalon.framework.configuration.Configuration ;
import org.apache.avalon.framework.configuration.ConfigurationException ;


/**
 * "Matching rules are used by servers to compare attribute values against 
 * assertion values when performing Search and Compare operations.  They are 
 * also used to identify the value to be added or deleted when modifying 
 * entries, and are used when comparing a purported distinguished name with the 
 * name of an entry."
 * 
 * @see <a href="http://www.faqs.org/rfcs/rfc2252.html">RFC 2252</a> Section 4.5
 * 
 * @author <a href="mailto:aok123@bellsouth.net">Alex Karasulu</a>
 * @author $Author: akarasulu $
 * @version $Revision: 1.2 $
 */
public class MatchingRuleImpl implements MatchingRule
{
    /** 
     * Transient definition for this syntax as specified in RFC 2252.
     * <code>
     * "(" whsp numericoid whsp
     *    [ "NAME" qdescrs ]
     *    [ "DESC" qdstring ]
     *    [ "OBSOLETE" whsp ]
     *    "SYNTAX" numericoid
     * whsp ")"
     * </code>
     */
    private transient String m_definition ;
    
    /** SyntaxImpl associated with this matching */
    private final Syntax m_syntax ;
    /** Object identifier of the matching rule */
    private final String m_oid ;
    /** Descriptive name for the matching rule */
    private String m_name ;
    /** Long description for the matching rule */
    private String m_desc ;
    /** Whether or not this matching rule is obsoleted by another */
    private boolean m_isObsolete = false ;

    /** Normalizer used to enable this matching rule */
    private final Normalizer m_normalizer ;
    /** Comparator used to enable this matching rule */
    private final Comparator m_comparator ;
    
    
    // ------------------------------------------------------------------------
    // C O N S T R U C T O R
    // ------------------------------------------------------------------------
    
    
    /**
     * Creates a MatchingRule instance with a unique object identifier.
     * 
     * @param a_oid the object identifier of the new MatchingRule.
     * @param a_syntax the syntax used by this matching rule
     * @param a_comparator the comparator used if this matching rule is used to
     *      determine ordering
     * @param a_normalizer the normalizer used if this matching rule is used to
     *      determine a match
     */
    MatchingRuleImpl( String a_oid, Syntax a_syntax, Comparator a_comparator, 
        Normalizer a_normalizer )
    {
        m_oid = a_oid ;
        m_syntax = a_syntax ;
        m_comparator = a_comparator ;
        m_normalizer = a_normalizer ;
    }
    
    
    // ------------------------------------------------------------------------
    // Member Accessor Mutator Methods
    // ------------------------------------------------------------------------
    
    
    /**
     * Gets a description of this MatchingRule.
     * 
     * @return a description
     */
    public String getDesc()
    {
        return m_desc ;
    }


    /**
     * Sets the description for this MatchingRule.
     * 
     * @param a_desc the description
     */
    void setDesc( String a_desc )
    {
        m_desc = a_desc ;
    }


    /**
     * Gets a short descriptive name for the MatchingRule. 
     * 
     * @return a short name.
     */
    public String getName()
    {
        return m_name ;
    }


    /**
     * Sets the name or label for this MatchingRule.
     * 
     * @param a_name a short name or label
     */
    void setName( String a_name )
    {
        m_name = a_name ;
    }


    /**
     * Gets the oid for this MatchingRule.
     * 
     * @return the object identifier
     */
    public String getOid()
    {
        return m_oid ;
    }
    

    /**
     * Gets the SyntaxImpl used by this MatchingRule.
     * 
     * @return the SyntaxImpl of this MatchingRule
     */
    public Syntax getSyntax()
    {
        return m_syntax ;
    }


    /**
     * Gets whether or not this MatchingRule has been obsoleted for another.
     * 
     * @return true if it is obsolete false otherwise
     */
    public boolean isObsolete()
    {
        return m_isObsolete ;
    }
    

    /**
     * Sets whether or not this MatchingRule has been obsoleted for another.
     * 
     * @param a_isObsoleted true if it is obsolete false otherwise
     */
    void setObsolete( boolean a_isObsoleted )
    {
        m_isObsolete = a_isObsoleted ; 
    }


    /**
     * Gets the Comparator enabling the use of this MatchingRule for ORDERING
     * and sorted indexing. 
     * 
     * @return the ordering Comparator 
     */
    public Comparator getComparator()
    {
        return m_comparator ;
    }
    

    /**
     * Gets the Normalizer enabling the use of this MatchingRule for EQUALITY
     * matching and indexing. 
     * 
     * @return the ordering Comparator 
     */
    public Normalizer getNormalizer()
    {
        return m_normalizer ;
    }


    /**
     * Gets or generates the MatchingRuleDescription for this MatchingRule as 
     * defined by the SyntaxImpl 1.3.6.1.4.1.1466.115.121.1.30.
     * 
     * <code>
     * "(" whsp
     *    numericoid whsp
     *    [ "NAME" qdescrs ]
     *    [ "DESC" qdstring ]
     *    [ "OBSOLETE" whsp ]
     *    "SYNTAX" numericoid
     * whsp ")"
     * </code>
     * @return the MatchingRuleDescription string
     */
    public String getMatchingRuleDescription()
    {
        if ( null == m_definition )
        {
            StringBuffer l_buf = new StringBuffer() ;
            l_buf.append( "( " ) ;
            l_buf.append( m_oid ) ;
            
            if ( null != m_name )
            {
                l_buf.append( " NAME " ) ;
                l_buf.append( m_name ) ;
            }
            
            if ( m_isObsolete )
            {
                l_buf.append( " OBSOLETE " ) ;
            }
            
            if ( null != m_desc )
            {
                l_buf.append( " DESC " ) ;
                l_buf.append( m_desc ) ;
            }
            
            l_buf.append( " SYNTAX " ) ;
            l_buf.append( m_syntax.getOid() ) ;
            l_buf.append( " )" ) ;
            
            m_definition = l_buf.toString() ;
        }
        
        return m_definition ;
    }

    
    /**
     * Builds MatchingRule instances from Configuration nodes and Attributes. 
     * 
     * @author <a href="mailto:aok123@bellsouth.net">Alex Karasulu</a>
     * @author $Author: akarasulu $
     */
    public static class Builder
    {
        /**
         * Creates a matching rule when all information is specified rather than
         * kept within a configuration or a backend.
         * 
         * @param a_oid the oid of the MatchingRule 
         * @param a_syntax the Syntax used by the MatchingRule
         * @param a_comparator the Comparator used by the MatchingRule
         * @param a_normalizer the Normalizer used by the MatchingRule
         * @return a class instance that implements the MatchingRule interface
         */
        public static MatchingRuleImpl create( String a_oid, Syntax a_syntax, 
            Comparator a_comparator, Normalizer a_normalizer )
        {
            return new MatchingRuleImpl( a_oid, a_syntax, a_comparator, 
                a_normalizer ) ;
        }
        
        
        /**
         * Creates a MatchingRule instance using a matching-rule Configuration 
         * node.
         * 
         * @param a_config the matching-rule Configuration node
         * @param a_syntaxManager the SyntaxManager service to lookup a Syntax
         * @return the MatchingRule represented by the Configuration node
         * @throws ConfigurationException if the Configuration is invalid
         */
        public static MatchingRuleImpl create( Configuration a_config, 
            SyntaxManager a_syntaxManager ) throws ConfigurationException
        {
            String l_oid = a_config.getChild( "oid" ).getValue() ; 
            Syntax l_syntax = null ;
            MatchingRuleImpl l_rule = null ;
            Comparator l_comparator = null ; 
            Normalizer l_normalizer = null ; 
            Configuration l_node = a_config.getChild( "normalizer", false ) ;
             
            try
            {
                l_syntax = a_syntaxManager.lookup( a_config
                    .getChild( "syntax" ).getValue() ) ;
            }
            catch ( NamingException e )
            {
                throw new ConfigurationException( 
                    "Failed on satisfing syntax dependency for MatchingRule "
                    + l_oid, e ) ;
            }
             
            if ( null == l_node )
            {
                l_normalizer = new PassThroughNormalizer( l_oid ) ;
            }
            else if ( null != l_node.getChild( "built-in", false ) )
            {
                try 
                {
                    l_normalizer = BuiltIns.getNormalizer( l_oid, 
                        l_node.getChild( "built-in" ).getValue() ) ; 
                } 
                catch ( NamingException ne )
                {
                    throw new ConfigurationException( "Unable to initialize "
                        + "matching rule " + l_oid + " due to unrecognized "
                        + "built-in tag value", ne ) ;
                }
            }
            else
            {
                throw new ConfigurationException( "Unrecognized configuration "
                    + "for matching rule " + l_oid + " normalizer" ) ;
            }
            
            l_node = null ;
            l_node = a_config.getChild( "comparator", false ) ;
            if ( null == l_node )
            {
                throw new ConfigurationException( "Comparator needed for " 
                    + "matching rule " + l_oid ) ;
            }
            else if ( null != l_node.getChild( "built-in", false ) )
            {
                try
                {
                    l_comparator = BuiltIns.getComparator( l_node
                        .getChild( "built-in" ).getValue() ) ;
                }
                catch ( NamingException ne )
                {
                    throw new ConfigurationException( "Failed to setup "
                        + "comparator for matching rule " + l_oid,  ne ) ;
                }
            }
            else
            {
                throw new ConfigurationException( "Unrecognized configuration "
                    + "for matching rule " + l_oid + " comparator" ) ;
            }
            
            l_rule = new MatchingRuleImpl( l_oid, l_syntax, l_comparator, 
                l_normalizer ) ;
            l_rule.setDesc( a_config
                .getChild( "description" ).getValue( null ) ) ;
            l_rule.setObsolete( a_config
                .getChild( "obsolete" ).getValueAsBoolean( false ) ) ;
            l_rule.setName( a_config.getChild( "name" ).getValue( null ) ) ;
                        
            return l_rule ;
        }
        
        
        /**
         * Creates a MatchingRule instance using an entry's Attributes.  
         * 
         * @todo Add support for normalizers and comparators via regular 
         * expressions and predefined functions.
         * 
         * @param a_attributes the set of attributes of a MatchingRule entry
         * @param a_syntaxManager the SyntaxManager service to lookup a Syntax
         * @return the MatchingRule represented by the entry
         * @throws NamingException if the required attributes cannot be accessed
         * or are unavailable
         */
        public static MatchingRuleImpl create( Attributes a_attributes, 
            SyntaxManager a_syntaxManager ) throws NamingException
        {
            String l_oid = null ;
            Syntax l_syntax = null ;
            MatchingRuleImpl l_rule = null ;
            Attribute l_attr = a_attributes.get( "oid" ) ;
            Comparator l_comparator = null ; 
            Normalizer l_normalizer = null ; 

            if ( null == l_attr || 0 == l_attr.size() )
            {
                throw new NamingException( "oid is undefined for " 
                    + a_attributes ) ;
            }
            l_oid = ( String ) l_attr.get() ;
            
            // Handle SyntaxImpl Configuration
            l_attr = a_attributes.get( "syntax" ) ;            
            if ( null == l_attr || 0 == l_attr.size() )
            {
                throw new NamingException( "syntax is undefined for " 
                    + a_attributes ) ;
            }
            l_syntax = a_syntaxManager.lookup( ( String ) l_attr.get() ) ;

            // Handle Normalizer Configuration
            if ( true )
            {
                throw new NotImplementedException( "NormalizerConfiguration" ) ;
            }

            // Handle Comparator Configuration
            if ( true )
            {
                throw new NotImplementedException( "ComparatorConfiguration" ) ;
            }

            l_rule = new MatchingRuleImpl( l_oid, l_syntax, l_comparator, 
                l_normalizer ) ;

            l_attr = a_attributes.get( "description" ) ;
            if ( null != l_attr )
            {
                l_rule.setDesc( ( String ) l_attr.get() ) ;
            }
            
            l_attr = a_attributes.get( "name" ) ;            
            if ( null != l_attr )
            {
                l_rule.setName( ( String ) l_attr.get() ) ;
            }
            
            l_attr = a_attributes.get( "obsolete" ) ;            
            if ( null != l_attr )
            {
                l_rule.setObsolete( getBoolean( l_attr ) ) ;
            }
            
            return l_rule ;
        }


        /**
         * Gets a boolean from an Attribute.
         * 
         * @param a_attr the attribute to convert to a boolean
         * @return true if attribute has a '1', 'true' or 'yes' value false
         * otherwise
         * @throws NamingException on failure to access attribute data
         */
        private static boolean getBoolean( Attribute a_attr )
            throws NamingException
        {
            String l_value = ( String ) a_attr.get() ;
            
            return l_value.equals( "1" ) || 
                l_value.equalsIgnoreCase( "true" ) ||
                l_value.equalsIgnoreCase( "yes" ) ;
        }
    }
}
