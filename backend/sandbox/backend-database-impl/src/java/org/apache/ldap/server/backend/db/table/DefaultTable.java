    public int count( Object a_key )
        throws NamingException
    {
        if ( ! m_allowsDuplicates ) 
        {
            if ( null == getRaw( a_key ) ) 
            {
                return 0 ;
            } 
            else 
            {
                return 1 ;
            }
        }

        TreeSet l_set = ( TreeSet ) getRaw( a_key ) ;

        if ( l_set != null ) 
        {
            return l_set.size() ;
        }

        return 0 ;
    }


